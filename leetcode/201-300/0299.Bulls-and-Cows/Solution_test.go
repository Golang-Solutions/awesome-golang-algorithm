package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name          string
		secret, guess string
		expect        string
	}{
		{"TestCase1", "1807", "7810", "1A3B"},
		{"TestCase2", "1123", "0111", "1A1B"},
		{"TestCase3", "1111", "1011", "3A0B"},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.secret, c.guess)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.secret, c.guess)
			}
		})
	}
}

